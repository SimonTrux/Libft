#include "libft.h"
#define ABS(x) x < 0 ? -x : x

size_t	ft_digit_count(int n)
{
	int i;

	i = 1;
	while (ABS(n) > 9)
	{
		n = (n / 10);
		i++;
	}
	return (i);
}
